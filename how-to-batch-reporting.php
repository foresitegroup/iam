<article>
  The Batch Reporting feature (available is advanced mode) allows users to produce a series of reports for a selected list of portfolios. For instance, users can customize batch reporting by using the various Report Setup Options to print a series of reports at the end of each month, each quarter or year.<br>
  <br>

  <img src="images/how-to-batch-reporting.jpg" alt=""><br>
  <br>

  Select Reports Menu, then Batch Reports.<br>
  <br>

  Users can define which portfolios to include, and which reports, for specific time periods. Provided in Investment Account Manager are these pre-defined reporting sets:<br>
  <br>

  <ul>
    <li>All Activity Dates: available for all reports and details transaction activity since inception.</li>
    <li>Year-End Tax Filing: available for the Income Received, Sold Securities, and Transaction Activity. Users can filter the transaction activity using a pre-defined or a custom date range.</li>
    <li>Before You Sell: available for all reports, but defaults with the Appraisal, Portfolio Diversification, Security Basis, and Sold Securities selected. Includes all activity since inception.</li>
    <li>Before You Buy: available for all reports, but defaults with the Appraisal and Portfolio Diversification selected. Includes all activity since inception.</li>
    <li>Diversification Reports: defaults with the Portfolio, Mutual Funds, Stock Industry and Stock Size Diversification reports selected. Includes all activity since inception.</li>
    <li>As of Date: available for all reports that use as-of-date pricing. Defaults with the Appraisal, Portfolio Diversification, Portfolio Performance, and Portfolio Summary selected. Includes all activity since inception to the as-of-date.</li>
  </ul>
  <br>

  User can also define and save a series of reports for repetitive use.
</article>

