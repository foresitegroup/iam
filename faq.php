  <strong><span class="redtext">Q:</span> What sort of asset types can be managed using Investment Account Manager?</strong><br>
  <br>

  As a comprehensive portfolio management software program, Investment Account Manager offers investment tracking features for a variety of asset types: cash, money market funds, US Governments, agencies, tax-exempt bonds, corporate bonds, preferred stocks, mutual funds, exchange traded funds, common stocks, options, and other investments. In the case of options, IAM is able to handle: opening buy, closing sale, opening sale, closing buy, assigned, exercised, and expired transactions.<br>
  <br>

  <hr>

  <br>

  <strong><span class="redtext">Q:</span> Does Investment Account Manager allow for multiple portfolio management?</strong><br>
  <br>

  Investment Account Manager is specifically designed to easily track an unlimited number of portfolios. If you have different types of portfolios: i.e. taxable accounts, IRAs, trust accounts, children accounts, etc., IAM will maintain a distinct portfolio for each account you own. You can collectively report on any/all portfolios using the IAM's report setup features.<br>
  <br>

  <hr>

  <br>

  <strong><span class="redtext">Q:</span> What sort of reporting tools are provided by Investment Account Manager?</strong><br>
  <br>

  Investment Account Manager includes more than 40 professional reports summarizing your investments: current holdings, unrealized gains/losses, sold positions &amp; realized gains/losses (tax filing), income received (tax filing), capital gain distributions, commissions paid, security allocation, portfolio diversification, portfolio cash flow projections, tax basis, performance measurements, asset maturity schedule, transactions ledger, and more. <a href="pdf/IAM_Sample_Reports.pdf">Click Here</a> for a PDF file of a few of the comprehensive reports provided by IAM.<br>
  <br>

  <hr>

  <br>

  <strong><span class="redtext">Q:</span> Does Investment Account Manager accurately derive investment performance?</strong><br>
  <br>

  Investment Account Manager calculates the modified IRR method to derive the internal rate of return for investment performance. This method calculates return for the period taking into effect the exact timing of each external cash flow. The model properly weights the timing of cash flows (additions/ withdrawals) for a portfolio or an individual asset, for a selected date range, to provide the return calculation. [CFA Institute Handbook Second Edition 2006]<br>
  <br>

  <hr>

  <br>

  <strong><span class="redtext">Q:</span> Does Investment Account Manager include any tutorial data to learn the program?</strong><br>
  <br>

  Investment Account Manager is provided with sample data and three program tutorials. By carefully reviewing the tutorials, you will understand how the program is intended to operate, while quickly learning how to utilize the program for your specific needs. All features in Investment Account Manager include detailed help and guidance. Features not covered in these tutorials can be quickly learned using the available online help. Free technical support is also provided while using IAM during your trial period.<br>
  <br>

