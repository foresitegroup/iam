  <strong><span class="redtext">Q:</span> Does Investment Account Manager accurately derive investment performance?</strong><br>
  <br>

  Investment Account Manager calculates the modified IRR method to derive the internal rate of return for investment performance. This method calculates return for the period taking into effect the exact timing of each external cash flow. The model properly weights the timing of cash flows (additions/ withdrawals) for a portfolio or an individual asset, for a selected date range, to provide the return calculation. [CFA Institute Handbook Second Edition 2006].  Investment Account Manager also allows users to store monthly, quarterly or annual IRR results of portfolios, and then link these returns for any sequential period for time weighting of sub-period IRR results.  <br>
