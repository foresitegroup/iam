  <br>

  <strong><span class="redtext">Q:</span> Can I download financial transactions from online financial institutions into Investment Account Manager?</strong><br>
  <br>

  Investment Account Manager is able to download transaction data from many leading financial institutions, including: A.G. Edwards, American Century, Charles Schwab, E*Trade, Fidelity Investments, Harris Direct, Quick &amp; Reilly, Robert W. Baird, Scottrade, TD Ameritrade, Vanguard, Wells Fargo, and myStreetScape (Muriel Siebert). Alternatively, IAM can also import any standard OFX (Open Financial Exchange) data file. If your financial institution is not listed, but you are able to download an OFX file directly, this file can be imported into IAM for transaction posting. Downloadable OFX files are often listed as Quicken QFX or MS Money spreadsheet files - either will work.<br>
