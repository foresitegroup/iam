<article class="how-to">
  Having the ability to monitor your portfolio holdings online is easy to accomplish with IAM and Yahoo Finance. Simply follow these steps.<br>
  <br>

  <strong>Steps to complete in Investment Account Manager</strong><br>
  <ol>
    <li>Open the portfolio you would like to track on Yahoo Finance.<br><br></li>
    <li>Print the Appraisal Report: select Reports | Appraisal Report | on the report setup screen, check the box for <em>Show Symbol, hide PE, Beta</em>.<br><br></li>
    <li>Click the Report button, then on the Report Preview Window, click the printer Icon. Close report.<br><br></li>
    <li>Select File Menu | Export | Security Symbol List | click on the radio button labeled Individual Portfolio.  Then use the drop-down menu to select the desired portfolio.<br><br></li>
    <li>Click the button labeled Copy to Clipboard.<br><br></li>
    <li>When complete, close the open windows and minimize Investment Account Manager.</li>
  </ol>
  <br>

  <strong>Steps to complete in Yahoo</strong><br>
  <ol>
    <li>Log-into Yahoo (<a href="http://www.yahoo.com">http://www.yahoo.com</a>)<br><br></li>
    <li>Either sign-in or sign-up for a new account.<br><br></li>
    <li>After logging into your account, select Finance from the left panel on Yahoo's Home Page.  This will take you to Yahoo Finance home page (<a href="http://finance.yahoo.com">http://finance.yahoo.com</a>).<br><br></li>
    <li>Point to My Portfolios Tab and then Create Portfolio.<br><br></li>
    <li>
      Next click on <em>Track your current holdings</em>.<br>
      <br>

      <strong>Step 1:</strong> Enter Portfolio name.<br>
      <br>

      <strong>Step 2:</strong> Symbols: Right-click you mouse in the input box, then Paste, which will copy the contents of your clipboard (i.e., the symbol list copied from IAM). Next, select any indices you would also like to monitor by checking the boxes of those desired.<br>
      <br>

      <strong>Step 3:</strong>  Add any indices.<br>
      <br>

      <strong>Step 3:</strong> Basic features:  You can skip this step.<br>
      <br>

      <img src="images/how-to-uploading-your-portfolio-to-yahoo-finance.jpg" alt=""><br>
      <br>

      <strong>If you wish to track your positions also, then...</strong><br>
      <br>

      <strong>Step 4:</strong> Advanced Features: use the default choices which are Shares Owned and Purchase Price / Share.<br>
      <br>

      Click the Continue button.<br>
      <br>

      <strong>Step 5:</strong> Enter the details using the Appraisal Report printed from IAM.  For the Price per Share column, use the Unit cost found on the Appraisal Report.  Do not fill in the fields for the indices you selected (these are shown with a ^). <em>NOTE: do not use the enter key to advance to another data field &mdash; click your mouse in the field to enter or edit. If you advance to the listing of your portfolio on Yahoo's <strong>My Portfolios Page</strong>, and you haven't completed the set-up, look for and click on "edit" which is found to the right of portfolio name. This will bring you back to the portfolio setup screens.</em>
    </li>
  </ol>
  <br>

  When you have finished entering the portfolio data, then click the Finished Button.<br>
  <br>

  Yahoo Finance will take you to the My Portfolios page.  Click on your internet browser's Refresh button to update the portfolio.<br>
  <br>

  <strong>That's it... you now can monitor your portfolio from anywhere in the world!</strong>
</article>

