<article class="how-to">
  In order for a portfolio management tool to be effective, it is essential to thoroughly provide a collective understanding of how your portfolios are integrated, and how the overlap.  This information will allow you to analyze two important questions:<br>
  <br>

  <i class="fa fa-check" aria-hidden="true"></i> Collectively, am I over/under allocated?<br>
  <i class="fa fa-check" aria-hidden="true"></i> Am I diversified?<br>
  <i class="fa fa-check" aria-hidden="true"></i> Do I hold concentrated positions?<br>
  <i class="fa fa-check" aria-hidden="true"></i> Are the securities I own held in the right accounts &mdash; i.e., in deferred vs. taxable?<br>
  <br>

  Investment Account Manager allows users to track an unlimited number of portfolios, each in its unique database file, transactions are segregated by account.  This provides the user the ability to design portfolios by objective, and to track by individual investment account.<br>
  <br>

  From collective perspective, IAM helps allows the combination of portfolios on the program's many reports.  This powerful ability keeps the user focused on multiple investment accounts relate.<br>
  <br>

  If for example, you have both tax deferred and currently taxable accounts within Investment Account Manager, and you were interested in better understanding the overall asset allocation of all of your holdings, you would select Investment Account Manager's Asset Allocation Report and include all of your portfolios on that report using the report setup screen.  Within seconds, Investment Account Manager will produce a report showing your combined portfolios' allocation by asset class (cash, bonds, stocks and other), by stock industry sector, and by stock size (large, medium or small cap).<br>
  <br>

  Within Investment Account Manager:<br>
  <ol class="spaced">
    <li>Select the report that is of interest to you from IAM's Reports Menu.</li>
    <li>On the Report Setup Window, select which portfolios you wish to include on the report.</li>
    <li>If you notice a Blue Hyperlink, click on it to provide information (ie, allocation objectives for the combination.</li>
    <li>Generate the report.</li>
  </ol>

