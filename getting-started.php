  <h5>GETTING STARTED</h5>

  Installing the free 30 day demo version of Investment Account Manager 3 Individual:<br>
  <br>

  <ol class="getting-started">
    <li class="long-string"><strong><a href="download-iam.php">Download the 30 day trial version</a> from our website &mdash; www.investmentaccountmanager.com.  The file name is IAM3_Setup.exe.</strong></li>

    <li>
      <strong>Install the trial.</strong>
      <ul>
        <li>Carefully read the License Agreement &mdash; IAM3 Individual is not intended for commercial use. <em>If you are a commercial/professional user, please see our Pro Version.</em></li>
        <li>Additionally, we allow for two installations of the software per user. This is to assure that you have access to IAM.</li>
        <li>We recommend using the default settings while installing IAM.</li>
        <li>IAMv3 will run on any Windows based system. IAMv3 is not intended to run on
networks. IAM can run on Apple operating systems, emulating Windows.</li>
      </ul>
    </li>

    <li>
      <strong>Launch Investment Account Manager v3 Individual</strong>
      <ul>
        <li>Complete the Welcome Screen by entering the name of the person to whom the software will be registered.</li>
        <li>Note that the software demo has a 30 day free trial period.</li>
      </ul>
    </li>

    <li>
      <strong>The IAMv3 Home Page</strong>
      <ul>
        <li>You should now be looking at IAM's Home Page.  Note that the window includes drop-down menus, a tool bar, and tabs. The home page allow for easy navigation through the various windows and reports found in IAMv3.</li>
        <li>
          Select the Help Menu | Contents Choice.
          <ul>
            <li>In the left-hand help window pane, select the book titled Chapter 1
Introduction.</li>
            <li>Select the book titled Home Page Intro.</li>
            <li>We suggest reviewing the various topics describing the IAMv3 home page
features and tab views.</li>
            <li>You can view a tutorial video of the IAM3 Individual home page on the <a href="video-tutorials.php">tutorial page</a>.
          </ul>
        </li>
      </ul>
    </li>

    <li>
      <strong>Reviewing IAM v3 Sample Reports</strong><br>
      <ul>
        <li>
          Select the Help Menu | Contents Choice
          <ul>
            <li>In the left-hand help window pane, select the book titled Chapter 1 Introduction</li>
            <li>Select the book titled Reports and Graphs Intro.</li>
            <li>Select the help topic IAM Reports and Graphs.</li>
            <li>We suggest using the Print icon to print this help topic and then to follow this as a guide to review reports using the IAM Demo Portfolio 3 for tutorial purposes.</li>
            <li>Once you've completed reviewing the help topic guide, select other reports as desired (note that you can combine individual portfolios onto a single report, for cumulative reporting. Very helpful for the "big picture" view).</li>
          </ul>
        </li>
      </ul>
    </li>

    <li>
      <strong>Reviewing Other IAM v3 Sample Data</strong><br>
      <ul>
        <li>
          First, update the prices of the sample securities (IAM includes access to free 20 minute delayed pricing from Yahoo! Finance and others).
          <ul>
            <li>Click on Update Prices button found in Tool Bar.</li>
            <li>If any sample price alerts have been triggered, you'll be prompted to review these.</li>
            <li>Once prices have been updated, use the OK button to return to the Home Page.</li>
            <li>
              The left panel of the Home Page provides access to the different sample portfolios provided with the IAMv3 demo version.
              <ul>
                <li>Click on another portfolio to change to that portfolio, i.e., open that portfolio for the IAMv3 home page display.</li>
              </ul>
            </li>
          </ul>
        </li>
      </ul>
    </li>

    <li>
      <strong>Entering Sample Data</strong><br>
      IAM allows users to either download their data from financial institutions, to manually enter their data using ledgers and windows, or to import their data from third party applications. A complete description of these methods is provided in the online help, as well as the online manual.<br>
      <br>

      For this quick setup guide, we'll take a look at manual entry using the IAMv3 Activity Ledger. You can view a tutorial video of the IAM3 Activity Ledger on the <a href="video-tutorials.php">tutorial page</a>.<br>
      <br>

      <ul>
        <li>
          Select the Transactions Menu.
          <ul>
            <li>Select Add, Edit, Delete.</li>
            <li>The transactions ledger provides users with a method to manually enter their data in ledger format, left-to-right, using drop downs to select transaction types, etc. Click on the next empty line in the ledger to try entering the following.</li>
            <li>Verify the radio button above the column labeled Security is selected to show All Securities.</li>
            <li>Click your mouse in the column labeled Security, and use the drop down to select a security provided with the IAMv3 sample demo data (securities are added as you build your portfolios).</li>
            <li>
              Select Caterpillar Inc.
              <ul>
                <li>Under the Activity column select Purchase.</li>
                <li>Under the Quantity column enter 100.</li>
                <li>In the column labeled Unit Price, enter 97.50.</li>
                <li>In the column Comm (commissions and fees), enter 10.00</li>
                <li>Notice the Dollar Amount is automatically populated for the cost of this purchase activity.</li>
                <li>Prior to selecting the Save button, notice in the lower left-hand corner a withdrawal is selected for the money market offset for this purchase transaction. (This is known as sweeping or cash pooling). In the lower right portion of the activity ledger, users can define the offset settlement date to use trade date + 3 or settlement date = trade date.</li>
                <li>Select the Save button.</li>
                <li>Select the Exit button to close the activity ledger.</li>
              </ul>
            </li>
          </ul>
        </li>
        <li>The other method to enter transactions we call our "Classic View" (Transactions Menu | Add Edit Delete | Classic View). This method provides sequential data entry screens to record your transactions. You can try this enrty method as well, referring to the help topics as you go along.</li>
      </ul>
    </li>

