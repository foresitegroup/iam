<article>
  <strong>So, let's get started.</strong><br>
  <br>

  <strong>If you haven't installed the trial version of IAM do so now. IAM will run on any Windows based system. IAM is not intended to run on networks.</strong><br>
  <span class="redtext">*</span> Note: IAM can run on Apple operating systems, emulating Windows.<br>
  <br>
  <br>

  <h5>INSTALLING IAM3</h5>

  <ol class="getting-started">
    <li class="long-string"><strong>Download the 30 day trial version from our website &mdash; www.investmentaccountmanager.com.  The file name is IAM3_Setup.exe.</strong></li>

    <li>
      <strong>Install the trial.</strong>
      <ul>
        <li>Carefully read the License Agreement &mdash; IAM3 is not intended for commercial use.  If you are a commercial user, please see our Pro Version.</li>
        <li>Additionally, we allow for two installations of the software per user.  This is to assure that you have access to IAM.</li>
        <li>We recommend using the default settings while installing IAM.</li>
      </ul>
    </li>

    <li>
      <strong>Launch Investment Account Manager</strong>
      <ul>
        <li>If asked to download previous month-end prices, click on yes.</li>
        <li>Complete the Welcome Screen by entering the name of the person to whom the software will be registered.</li>
        <li>Note that the software demo has a 30 day free trial period.</li>
        <li>
          Since we frequently update IAM, please click on the <strong>Check for Updates</strong> button found in the tool bar.<br>
          If a newer version of IAM is found, please download it.
          <ul>
            <li>After the download is complete, exit IAM.</li>
            <li>Click on the Windows Start Menu.</li>
            <li>Click on All Apps (All Programs).</li>
            <li>Find and click on Investment Account Manager 3 program group.</li>
            <li>Click on Update IAM and install the update.</li>
            <li>Start the demo again, now updated, by double-clicking on the IAM3 desktop Icon.</li>
          </ul>
        </li>
      </ul>
    </li>

    <li>
      <strong>Back to the updated program... IAM's Home Page</strong>
      <ul>
        <li>
          You should now be looking at IAM's Home Page.  Note that the window includes drop-down menus, a tool bar, and tabs. The home page allow for easy navigation through the various windows and reports found in IAM.
          <ul>
            <li>Drop-down menus provide access to main features.</li>
            <li>Tool-bar provides quick access to commonly used features.</li>
            <li>Tabs provide different views of various views of the open portfolio</li>
          </ul>
        </li>
      </ul>
    </li>

    <li>
      <strong>Review IAM's sample data included in the demo</strong><br>
      <ul>
        <li>
          First, update the prices of the sample securities (IAM includes access to free 20 minute delayed pricing from Yahoo! Finance and others).
          <ul>
            <li>Click on Update Prices button found in Tool Bar.</li>
            <li>Notice IAM will offer a chance to review price --alerts (something you will learn to setup later to notify you of important price changes).</li>
            <li>After updating, use Exit button to return to the Home Page.</li>
            <li>
              The left panel of the Home Page provides access to the different portfolios being monitored w/I IAM
              <ul>
                <li>Click on portfolio to change to that portfolio, i.e., open that portfolio.</li>
              </ul>
            </li>
          </ul>
        </li>
      </ul>
    </li>

    <li>
      <strong>Experiment using the different Home Page tabs by clicking on a tab. Each tab is designed to quickly provide you with specific information.</strong>
      <ul>
        <li>
          Many of the tabs can be customized for reporting period.<br>
          * Note: use Help buttons for a detailed explanation for the open window (IAM provides a detailed help system via the Help drop-down menu also).
        </li>
      </ul>
    </li>

    <li>
      <strong>Review IAM's reporting capabilities.</strong>
      <ul>
        <li>
          Select the Reports drop-down Menu.
          <ul>
            <li>
              Select Portfolio reports, then Appraisal Report.
              <ul>
                <li>Review the standard report set-up window.  Note that you can combine individual portfolios onto a single report, for cumulative reporting. Very helpful for the "big picture" view.</li>
                <li>Click on the Report button.</li>
              </ul>
            </li>
            <li>Select other reports as desired.</li>
          </ul>
        </li>
      </ul>
    </li>

    <li>
      <strong>Entering data into IAM.</strong> IAM allows users to either download their data from financial institutions, to manually enter their data using ledgers and windows, or to import their data from third party applications. A complete description of these methods is provided in the online help, as well as the online manual.
      <ul>
        <li>
          For this quick setup guide, we'll take a look at manual entry.
          <ul>
            <li>
              Select Transactions Menu.
              <ol type="a">
                <li><span>
                  Select Add, Edit, Delete.
                  <ul>
                    <li>The transactions ledger provides users with a method to manually enter their data in ledger format, left-to-right, using drop downs to select transaction types, etc. Click on the nest empty line in the ledger to try entering the following.</li>
                    <li>Under security use drop down to select a security previously entered in the demo (securities are added as you build your portfolios). Select Caterpillar Inc.</li>
                  </ul></span>
                </li>
                <li><span>Under the Activity column select purchase.</li></span>
                <li><span>Under the Quantity column enter 100.</li></span>
                <li><span>Under the Dollar Amount column, enter the 6650.00 (net amount of purchase) then use the enter key and click on save.</li></span>
                <li><span>The demo uses a money market offset &mdash; notice a withdrawal is automatically made for the amount of the trade (This is known as sweeping or cash pooling).</li></span>
              <li><span>Click the OK button found at the bottom of the ledger to save and close.</span></li>
            </ol>
          </li>
        </ul>
      </li>
      <li>
        The other method to enter transactions we call our "Classic View" (we have developed portfolio management software since 1985). This method provides sequential data entry screens to record your transactions. Under this method, we first review the Activity Ledger to check what has been previously entered.
        <ul>
          <li>Click on the Enter New button.</li>
          <li>Enter the date.</li>
          <li>Select the security from the list of previously entered securities, or Add New in the drop-down list.</li>
          <li>Scroll down to select Caterpillar.</li>
          <li>Click on Purchase button.</li>
          <li>Enter 100 as the quantity.</li>
          <li>6850 as the net amount.</li>
          <li>Click on the Save button.</li>
          <li>Click on the red-x in the upper-right corner of the Activity Ledger window.</li>
        </ul>
      </li>
    </ul>
  </li>

  <li>
    Entering your portfolios into IAM is easy and flexible using the Portfolio Setup Wizard found in the toolbar.  It provides several methods to start tracking our accounts. <strong>Click on the Portfolio Wizard button</strong>.
    <ul>
      <li>Enter a portfolio name (portfolio is synonymous with investment account).  Example might be Peter's 401K.</li>
      <li>
        Select one of the methods to proceed.  We suggest that you use the Learn More links to help you decide. Most new users start with their current portfolio balances. All that is needed is a summary of what you own, when you bought it, and what you paid.<br>
        NOTE: it's worth it to accurately enter the lot-by-lot data of your current holdings Since IAM provides detailed tax lot accounting as well as detailed performance measurements.
        <ul>
          <li>IAM allows for an unlimited number of portfolios</li>
          <li>IAM allows for easy deletion of portfolios</li>
        </ul>
      </li>
    </ul>
  </li>

