  <br>

  The <strong>Copy a Security to another Portfolio</strong> choice is used to copy all the transaction records (purchases, sales, income, etc.) of one security in a portfolio, to another portfolio being monitored within Investment Account Manager. This choice copies the entire transaction history from the original portfolio to the destination portfolio.  It allows you to remove the security from the original or not.<br>
  <br>

  The <strong>Transfer a Security to another Portfolio</strong> choice is used to:<br>
  <br>
  <ol>
    <li>Transfer either a portion of a security holding, or an entire position, to another portfolio being monitored within Investment Account Manager.<br><br></li>
    <li>It can also be used to deliver holdings out of Investment Account Manager (i.e. to a charity).</li>
  </ol>
  <br>

  The transfer feature can also be used to step up the basis for inherited shares from one portfolio to another portfolio.  The feature allows the user to select the basis to assign to the transferred securities:<br>
