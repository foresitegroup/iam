<article class="how-to">
  <strong><em>Definition:</em></strong> the Price Alerts feature is a valuable tool found within Investment Account Manager to keep users notified when the market price of an asset crosses its high or low target price.<br>
  <br>

  To enter or update price alerts select Asset Library Menu | Price Alerts.<br>
  <br>

  Each time market prices are updated, this feature automatically checks the updated market prices against your previously entered high/low price targets.  The price alerts screen then indicates in color which price target has been crossed: if the high alert has been exceeded, the high alert price shows in green; if the low alert has been crossed, the low alert price shows in red.<br>
  <br>

  You can sort securities (ascending or descending order) by placing your mouse pointer over a column label and using a left-double click.
  <br>

  <ul>  
    <li>Use the <strong>Edit Tab</strong> to change price alerts for a selected security.<br><br></li>
    <li>Use the <strong>Security Search Tab</strong> to list all portfolios within Investment Account Manager currently holding open positions for the highlighted asset, i.e. all portfolios cross-referenced for the selected asset.</li>
  </ul>
  <br>

  Users also have the following choices:<br>
  <br>

  <ul>
    <li>Print: print a report listing all existing securities with price alert limits.<br><br></li>
    <li>Internet Research: immediately access the Internet for free stock research and news. You can find fundamental variables, recent quotes, industry classifications, company news, and much more for the currently selected security.<br><br></li>
    <li>Set Alert Range: users can globally set price alert targets as a percentage above (high) and / or a percentage below (low) the current market price of securities. For example, users could enter 15% as the percentage above and 10% as the percentage below for price targets. Security price alerts will be automatically derived and entered to coincide with these percentage settings. This is particularly helpful to closely relative market price changes.<br><br></li>
    <li>Remove All Alerts: use this choice to remove all existing price alerts from securities entered.</li>
  </ul>
</article>

