    In order for a portfolio management tool to be effective, an essential requirement includes the ability to provide a collective understanding of how your portfolios are integrated, and how they overlap.  This information will allow you to analyze several important questions:<br>
    <ul style="text-align: left; display: inline-block; margin: 0 auto;">
      <li>Collectively, what is my overall allocation, and am I properly diversified?</li>
      <li>Do I hold concentrated positions?</li>
      <li>Are the securities I own held in the right accounts &mdash; i.e., in deferred vs. taxable?</li>
    </ul>

    <hr>

    <img src="images/tour-iam-1-2.jpg" alt="">

    <hr>

    Investment Account Manager allows users to track an unlimited number of portfolios, with transactions segregated by account.  This provides the ability to create and track portfolios by investment account, and to design portfolios by investment objective.<br>
    <br>

    Investment Account Manager is able to group portfolios while creating reports.  For example, if you have taxable and tax deferred accounts within Investment Account Manager, and want to review the overall allocation of all of your holdings, you would select one of the available allocation reports provided within Investment Account Manager.  Investment Account Manager will produce a report showing your combined portfolios' allocation by asset class (cash, bonds, stocks and other), by stock sector (consumer discretionary v. healthcare v. energy, etc.), and/or by stock capitalization (large, medium or small cap).  With this information in hand, you can determine if you are meeting the objectives you have set for your individual portfolios, as well as for all of your accounts.<br>
    <br>

    Investment Account Manager includes more than 50 informative and detailed reports summarizing your investments: current holdings, unrealized gains/losses, sold positions &amp; realized gains/losses (tax filing), income received (tax filing), capital gain distributions, commissions paid, portfolio allocations, portfolio cash flow projections, tax basis, performance measurements, asset maturity schedule, transactions ledger, and more. <a href="pdf/IAM_Sample_Reports.pdf">View a PDF</a> file of a few of the comprehensive reports provided by IAM.
