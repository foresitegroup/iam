<article>
  If you have been tracking your portfolio on Google (or another online service) it can be imported into Investment Account Manager 3, so long as the download file is an OFX type file.  Follow these steps to import:<br>
  <br>

  <ul>
    <li>Logon to Google Finance and select and open your portfolio.</li>
    <li>Click on the Performance tab.</li>
    <li>Click on Download to OFX.</li>
    <li>Note the file name and location</li>
    <li>Open IAM and select File Menu | Import | Broker OFX or QFX.</li>
    <li>Name the portfolio, and then click the next button.</li>
    <li>Enter the location and name of the OFX file (use browse button to find).</li>
    <li>Be sure the file has the OFX or QFX extension (i.e., test.ofx).</li>
    <li>Use IAM's import and post windows to update your portfolio.</li>
  </ul>
  <br>

  After the process is complete, open the new portfolio from IAM3's home page.<br>
