      <br>

      <img src="images/how-to-transferring-files-from-iam2-to-new-iam31.jpg" alt=""><br><br>
    </li>
    <li>Select the OK button when prompted to continue with the conversion of your IAM2 individual data into IAM3 Individual version.<br><br></li>
    <li>
      If you used the original default location for your IAM2 installation, your portfolios should be listed for conversion. Select the '<strong>Convert Now</strong>' button.<br>
      <br>

      <img src="images/how-to-transferring-files-from-iam2-to-new-iam32.jpg" alt=""><br>
      <br>

      <ul><li>Note:  If you did not use the original default location for IAM2, use the 'Directory' button to locate the installation directory for your current copy of IAM2, and then select the \Data folder. Your portfolios should be listed for conversion.  Select the '<strong>Convert Now</strong>' button.</li></ul>
      <br>
    </li>
    <li>All existing portfolio data will be migrated to IAM3, and IAM3 will be activated to a full version, provided your IAM2 support is up-to-date and active.<br><br></li>
