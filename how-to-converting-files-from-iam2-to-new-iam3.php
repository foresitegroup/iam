  <strong>Converting IAM2 data to IAM3</strong><br>
  Prior to converting your IAM2 data to IAM3, we recommend making a backup of your current IAM2 data.<br>
  <br>

  <ul>
    <li>Start your copy of IAM2, and use the file menu | backup data choice | backing up your data to a removable drive.<br><br></li>
    <li>Close IAM2.</li>
  </ul>
  <br>

  When you are ready, you can then convert your existing IAM2 data to IAM3.  Note: your IAM2 data will remain unchanged and intact for continued use in IAM2 to shadow entry activity while using IAM3.<br>
  <br>

  <ul>
    <li>Start the IAM3 demo.<br><br></li>
    <li>
