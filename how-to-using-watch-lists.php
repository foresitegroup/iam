<article>
  A watch list is a grouping of securities into a portfolio that have something in common.  For instance, you might have a watch list for your IRA portfolio, another for your Taxable.  Watch list can also be effectively grouped by types of securities: i.e., Energy Common Stocks, Financial Common Stocks, etc.  The point is, Investment Account Manager allows for an unlimited number of watch list portfolios so that you can keep up-to-date with alternative investments.
  Using watch lists help investors better manage their portfolios by providing alternative investment lists to use when building or modifying existing portfolios.<br>
  <br>

  Creating a watch list portfolio in IAM is easy - simply select the Portfolios Menu and add a new portfolio  (Repeat the process for as many as desired). After creating the portfolio Investment Account Manager simply add securities to each list.  Select the setup option for "Enter all historical investment transactions". Then advance through the subsequent screens until you see the Home Page for the New Watch List displayed.<br>
  <br>

  One popular method is to enter hypothetical purchases of 100 shares of each security to the watch list stock, using the market price on the date of addition as the cost per share.  This method will then tracks the progress of the watch lists stocks from that point forward, showing the price movement since the addition to the list (i.e., you'll be able to monitor the change in values based on the cost when added and the current market values).<br>
  <br>

  Watch lists offer effective "what-if" testing.  Users can gauge diversification and allocation impact of additions to an existing portfolio before actually purchasing new securities.  This can be easily accomplished by combing a purchase watch list with existing portfolios on any of Investment Account Managers reports (using the "Portfolio to include" option on the report setup screen).
</article>

