<article>
  Having the ability to monitor your portfolio holdings online is easy to accomplish with IAM and Google Finance. To do so, follow these simply steps.<br>
  <br>

  <strong>Steps to complete in Investment Account Manager</strong><br>
  <ol>
    <li>Open the portfolio you would like to track on Google Finance.<br><br></li>
    <li>From the IAM Home page, click on the Positions Tab.<br><br></li>
    <li>On the Portfolio Values Tab, click on the Export button.  Select Google.CSV.<br><br></li>
    <li>
      Windows Save window will appear.<br><br>
      <ol type="a">
        <li>For the filed Look in, enter a destination easily located - i.e., your Desktop. This is where the export file will be saved which will be imported into Google.<br><br></li>
        <li>For File name use the portfolio name or abbreviation.<br><br></li>
      </ol>
    </li>
    <li>Operation complete window will show after file is saved.<br><br></li>
    <li>You can now minimize Investment Account Manager 5.<br><br></li>
  </ol>
  <br>

  <strong>Steps to complete in Google</strong><br>
  <ol>
    <li>Log-into Google Finance  (<a href="http://www.google.com/finance">http://www.google.com/finance</a>)<br><br></li>
    <li>Either sign-in, or sign-up for a new account.<br><br></li>
    <li>After logging into your account, select Portfolios from the left panel on Google Finance Home Page.<br><br></li>
    <li>Click on Create a new portfolio (if your security software blocks access, allow the "scripted windows" to continue and then click on Create a new portfolio again).<br><br></li>
    <li>Enter the Portfolio , then OK.<br><br></li>
    <li>A message will show that the portfolio is empty. Find and click on Import Transactions.<br><br></li>
    <li>Google now asks for the file name to import.  Click on the browse button and find the file saved in IAM (step 4 above). Select the file and then OK to list the file name and location in the Import dialog box.<br><br></li>
    <li>Next click on the Preview Import button.<br><br></li>
    <li>Scroll down the window to find the Import button and click on Import.  You should now be viewing your portfolio.</li>
  </ol>
  <br>

  <img src="images/how-to-uploading-your-portfolio-to-google-finance.jpg" alt=""><br>
  <br>

  <strong>That's it... you now can monitor your portfolio from anywhere in the world!</strong>
</article>

